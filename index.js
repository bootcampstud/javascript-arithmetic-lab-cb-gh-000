function add(a,b){
    var something = a+b;
    return(something)
}
function subtract(a,b){
    var something = 1*(b-a);
    return(something)
}
function multiply(a,b){
    var something = b*a;
    return(something)
}
function divide(a,b){
    var something = b/a;
    return(something)
}

function inc(n){
  var newthing = n+1;
  return(newthing)
}
function dec(n){
  var newthing = n-1;
  return(newthing)
}
function makeInt(n){
  var newthing = parseInt(n,1);
  return(newthing)
}
